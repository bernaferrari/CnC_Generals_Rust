//-------------------------------------------------------------------------------------------------
/** Parse Music entry */
//-------------------------------------------------------------------------------------------------
void INI::parseVideoDefinition( INI* ini )
{
	// read the name
	const char* c = ini->getNextToken();

	Video video;
	video.m_internalName.set( c );

	ini->initFromINI(&video, TheVideoPlayer->getFieldParse() );
	TheVideoPlayer->addVideo(&video);


}  // end parseVideoDefinition


